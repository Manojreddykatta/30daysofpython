# int to float
num_int = 5
print('num_int', num_int)
num_float = float(num_int)
print('num_float', num_float)

# float to int
gravity = 9.81
print('gravity:', int(gravity))

# int to str
num_int = 10
print(num_int)
num_str = str(num_int)
print('num_str', num_str)
print(type(num_str))


# str to num and float
num_str = "10"
print('num_str', num_str)
num_float = float(num_str)
num_int = int(num_str)
print('num_int', num_int)
print('num_float', num_float)

